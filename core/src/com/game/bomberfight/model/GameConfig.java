package com.game.bomberfight.model;

public class GameConfig {
	private int winX;
	private int winY;
	
	
	public int getWinX() {
		return winX;
	}
	public void setWinX(int winX) {
		this.winX = winX;
	}
	public int getWinY() {
		return winY;
	}
	public void setWinY(int winY) {
		this.winY = winY;
	}
	
}
